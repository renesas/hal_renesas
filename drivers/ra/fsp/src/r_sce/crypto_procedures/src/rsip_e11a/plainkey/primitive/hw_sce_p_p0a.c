    {
        return FSP_ERR_CRYPTO_SCE_RESOURCE_CONFLICT;
    }

    WR1_PROG(REG_70H, 0x000a0001U);
    WR1_PROG(REG_4CH, 0x00000000U);

    HW_SCE_p_func100(0x3d03389eU, 0x3e942d5cU, 0x150c492bU, 0xd5cd8aacU);
    HW_SCE_p_func103();
