    WR1_PROG(REG_D0H, 0x0c200104U);
    WAIT_STS(REG_14H, 31, 1);
    WR1_PROG(REG_2CH, change_endian_long(0x00000000U));

    WR1_PROG(REG_9CH, 0x80010000U);
    WR1_PROG(REG_00H, 0x03410005U);
    WAIT_STS(REG_04H, 30, 0);
    WR1_PROG(REG_40H, 0x00001800U);
    WR1_PROG(REG_00H, 0x0001000dU);
    WAIT_STS(REG_04H, 30, 0);
    WR1_PROG(REG_40H, 0x00001800U);

    WR1_PROG(REG_94H, 0x000034c0U);

    WR1_PROG(REG_14H, 0x000000a7U);
    WR1_PROG(REG_9CH, 0x800100e0U);
    WAIT_STS(REG_14H, 31, 1);
    WR1_PROG(REG_2CH, change_endian_long(0x0000000aU));
    WR1_PROG(REG_24H, 0x00000000U);

    HW_SCE_p_func101(0x2eb385dbU, 0x30b9c4aeU, 0xbfef9e5eU, 0xdae0ba86U);
    HW_SCE_p_func043();

    WR1_PROG(REG_94H, 0x0000b4c0U);
    WR1_PROG(REG_94H, 0x0000001aU);

    WR1_PROG(REG_14H, 0x000000a7U);
    WR1_PROG(REG_9CH, 0x800100e0U);
    WAIT_STS(REG_14H, 31, 1);
    WR1_PROG(REG_2CH, change_endian_long(0x0000000aU));
    WR1_PROG(REG_24H, 0x00000000U);

    HW_SCE_p_func101(0x64e9820dU, 0xb87f2e3bU, 0xe82f80daU, 0x47ad9cb3U);
    HW_SCE_p_func044();

    HW_SCE_p_func100(0x7998b9e4U, 0x9359eb32U, 0x2b780124U, 0xe48ce084U);
    HW_SCE_p_func103();

    HW_SCE_p_func100(0x370e575eU, 0x5c09600bU, 0xe61ca789U, 0x827e89e1U);
    WR1_PROG(REG_14H, 0x000000a1U);
    WR1_PROG(REG_D0H, 0x0c200104U);
    WAIT_STS(REG_14H, 31, 1);
    WR1_PROG(REG_2CH, change_endian_long(0x00000000U));
