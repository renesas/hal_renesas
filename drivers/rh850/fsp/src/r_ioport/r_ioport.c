/*******************************************************************************************************************//**
 *
 * @note This function is enable for NF.
 * @retval FSP_SUCCESS               Select Input Signal Path
 * @retval FSP_ERR_ASSERTION         A critical assertion has failed
 * @retval FSP_ERR_INVALID_ARGUMENT  Invalid input parameter
 * @retval FSP_ERR_NOT_OPEN          The module has not been opened
 **********************************************************************************************************************/
fsp_err_t R_IOPORT_NoiseFilterEnable (ioport_ctrl_t * const p_ctrl, ioport_filter_signal_t filter_signal)
{
    fsp_err_t err = FSP_SUCCESS;
#if IOPORT_CFG_PARAM_CHECKING_ENABLE
    ioport_instance_ctrl_t * p_instance_ctrl = (ioport_instance_ctrl_t *) p_ctrl;
    FSP_ASSERT(NULL != p_instance_ctrl);
    FSP_ERROR_RETURN(IOPORT_OPEN == p_instance_ctrl->open, FSP_ERR_NOT_OPEN);
    FSP_ERROR_RETURN(IOPORT_DNF_ELEMENT(g_ioport_dnf_enable_table) > filter_signal, FSP_ERR_INVALID_ARGUMENT);
    FSP_ERROR_RETURN(NULL != g_ioport_dnf_enable_table[filter_signal].p_reg_dnfa_en, FSP_ERR_INVALID_ARGUMENT);
#endif

    /* Enable noise filter for input signal */
    *(g_ioport_dnf_enable_table[filter_signal].p_reg_dnfa_en) |=
        (1 << g_ioport_dnf_enable_table[filter_signal].bit);

    return err;
}

