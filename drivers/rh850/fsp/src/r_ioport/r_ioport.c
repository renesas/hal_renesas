/*******************************************************************************************************************//**
 *
 * @note This function is re-configuration for NF.
 * @retval FSP_SUCCESS                 Select Input Signal Path
 * @retval FSP_ERR_ASSERTION           A critical assertion has failed
 * @retval FSP_ERR_INVALID_ARGUMENT    Invalid input parameter
 * @retval FSP_ERR_NOT_OPEN            The module has not been opened
 **********************************************************************************************************************/
fsp_err_t R_IOPORT_NoiseFilterCfg (ioport_ctrl_t * const       p_ctrl,
                                   ioport_filter_signal_t      filter_signal,
                                   const ioport_filter_cfg_t * p_filter_cfg)
{
    fsp_err_t err = FSP_SUCCESS;
#if IOPORT_CFG_PARAM_CHECKING_ENABLE
    ioport_instance_ctrl_t * p_instance_ctrl = (ioport_instance_ctrl_t *) p_ctrl;
    FSP_ASSERT(NULL != p_instance_ctrl);
    FSP_ERROR_RETURN(IOPORT_OPEN == p_instance_ctrl->open, FSP_ERR_NOT_OPEN);
    FSP_ERROR_RETURN(IOPORT_DNF_ELEMENT(g_ioport_dnf_enable_table) > filter_signal, FSP_ERR_INVALID_ARGUMENT);
#endif
    ioport_filter_sample_t    number_sample  = p_filter_cfg->number_sample;
    ioport_filter_clock_div_t sampling_clock = p_filter_cfg->sampling_clock;
    ioport_filter_detection_t detection_mode = p_filter_cfg->detection_mode;
    ioport_filter_select_t    bypass_mode    = p_filter_cfg->bypass_mode;
    uint8_t dnfa_ctrl_value =
        (uint8_t) ((number_sample << R_DNFA_ENCA_DNFACTL_ENCA_NFSTS_Pos) |
                   (sampling_clock << R_DNFA_ENCA_DNFACTL_ENCA_PRS_Pos));
    uint8_t fcla_ctrl_value = (uint8_t) ((bypass_mode << R_FCLACTL_TAPA_FCLACTL0_TAPA_BYPS_Pos) |
                                         (detection_mode << R_FCLACTL_TAPA_FCLACTL0_TAPA_INTR_Pos));

    /* Configuration for Prescaler */
    *(g_ioport_dnf_enable_table[filter_signal].p_reg_dnfa_ctrl) = dnfa_ctrl_value;

    /* Configuration for Edge Detector */
    *(g_ioport_dnf_enable_table[filter_signal].p_reg_fcla_ctrl) = fcla_ctrl_value;

    return err;
}

